def LU(A, B):
    L = np.tril(A)
    U = np.triu(A)
    L[0][0] = 1
    L[1][1] = 1
    L[2][2] = 1

    L[1][0] = A[1][0]/U[0][0]
    U[1][1] = A[1][1]-(L[1][0]*U[0][1])
    U[1][2] = A[1][2]-(L[1][0]*U[0][2])
    L[2][0] = A[2][0]/U[0][0]
    L[2][1] = (A[2][1] - L[2][0]*U[0][1])/U[1][1]
    U[2][2] = A[2][2]-(L[2][0]*U[0][2] + L[2][1]*U[1][2])

    print(U, '\n')
    print(L, '\n')

    x = (B[0][0])/L[0][0]
    y = (B[0][1] - L[1][0]*x)/(L[1][1])
    z = (B[0][2] - L[2][0]*x - L[2][1]*y)/L[2][2]

    print('\n', x, y, z)

    z = (z/U[2][2])
    y = (y - U[1][2]*z)/U[1][1]
    x = (x - U[0][1]*y - U[0][2]*z)/U[0][0]

    print(x, y, z, '\n')

print("2 a")
